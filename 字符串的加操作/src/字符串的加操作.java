public class 字符串的加操作 {
    public static void main(String[] args){
        System.out.println("AShi" + "wuya");
        System.out.println("AShiwuya" + 666);
        //字符串可以和int类型进行加法操作
        System.out.println(666 + "AShiwuya");
        //调换也可以
        System.out.println("无涯" + 6 + 66);
        System.out.println(1 + 99 + "年无涯");
    }
}
